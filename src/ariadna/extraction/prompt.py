"""Системный промпт LLM-извлечения по онтологии — extraction/prompt.py (A-08).

Вход: нет (константа, вычисляется один раз при импорте). Выход: `SYSTEM_PROMPT`
(str) — описание 8 EntityType + 6 RelationType из contracts.py и требуемый
компактный JSON-формат ответа {"entities": [...], "relations": [...]}; и
`build_user_prompt(chunk_text)` — обёртка текста чанка в инструкцию.
Зависимости: только contracts.py (EntityType/RelationType — источник перечня
типов, чтобы промпт не разошёлся с онтологией при её изменении оркестратором).
Инвариант: промпт НЕ просит модель извлекать числа/единицы/constraints —
это зона правил (extraction/rules.py, инвариант №3) — и НЕ просит doc_id/
chunk_id/model — их проставляет код (llm_extract.py).
Паспорт: docs/dev/modules/extraction.md.
"""
from __future__ import annotations

from ariadna.contracts import EntityType, RelationType

# ─── Описания типов для промпта (кратко, RU) ───────────────────────────────
# Порядок и формулировки — по TASK.md/contracts.py; не расширять список типов
# (онтология зафиксирована заданием, contracts.EntityType/RelationType 🔒).
_ENTITY_TYPE_HINTS: dict[EntityType, str] = {
    EntityType.MATERIAL: "сырьё, материал, реагент, продукт (руда, шлак, электролит, штейн, концентрат)",
    EntityType.PROCESS: "процесс, технология, метод (обессоливание, электроэкстракция, флотация, плавка)",
    EntityType.EQUIPMENT: "оборудование, аппарат, установка (электролизёр, реактор, мембрана, печь)",
    EntityType.PROPERTY: "свойство, параметр, показатель БЕЗ значения (минерализация, температура, концентрация "
                          "сульфатов — само число НЕ извлекай, только имя параметра)",
    EntityType.EXPERIMENT: "эксперимент, испытание, кейс, промышленные/лабораторные испытания",
    EntityType.PUBLICATION: "публикация, источник, отчёт, статья, доклад",
    EntityType.EXPERT: "эксперт, автор, организация, институт, завод-разработчик",
    EntityType.FACILITY: "промышленный объект: завод, фабрика, рудник, установка на конкретной площадke",
}

_RELATION_TYPE_HINTS: dict[RelationType, str] = {
    RelationType.USES_MATERIAL: "Process/Equipment использует Material (сырьё, реагент)",
    RelationType.OPERATES_AT_CONDITION: "Process/Equipment работает при условии/параметре (Property) — числовые "
                                         "границы условия НЕ указывай, только связь с именем параметра",
    RelationType.PRODUCES_OUTPUT: "Process производит Material/Property (продукт, выход процесса)",
    RelationType.DESCRIBED_IN: "любая сущность описана в Publication (источник факта)",
    RelationType.VALIDATED_BY: "утверждение/сущность подтверждена Experiment (испытанием)",
    RelationType.CONTRADICTS: "две сущности/утверждения противоречат друг другу (разные источники — разные выводы)",
}


# Назначение: рендерит блок «тип — описание» для системного промпта.
# Уровень: ✅ реализовано (A-08)
def _render_type_hints() -> str:
    entity_lines = "\n".join(f'  - "{t.value}": {hint}' for t, hint in _ENTITY_TYPE_HINTS.items())
    relation_lines = "\n".join(f'  - "{t.value}": {hint}' for t, hint in _RELATION_TYPE_HINTS.items())
    return (
        f"Типы сущностей (EntityType, ровно эти 8 значений в поле \"type\"):\n{entity_lines}\n\n"
        f"Типы связей (RelationType, ровно эти 6 значений в поле \"type\"):\n{relation_lines}"
    )


# ─── SYSTEM_PROMPT ──────────────────────────────────────────────────────────
# Назначение: системный промпт LLM-извлечения — онтология + требование
#   строгого компактного JSON без обёрток и пояснений; вычисляется один раз
#   при импорте модуля (константа, не меняется между вызовами).
# Входные связи: contracts.EntityType/RelationType (через _render_type_hints)
# Выходные данные: str
# Уровень: ✅ реализовано (A-08)
SYSTEM_PROMPT = (
    "Ты — извлекатель знаний для карты R&D горно-металлургической отрасли "
    "(GraphRAG). По фрагменту текста (RU, иногда с англ. терминами) извлеки "
    "сущности и связи между ними СТРОГО по заданной онтологии.\n\n"
    f"{_render_type_hints()}\n\n"
    "Правила:\n"
    "1. Извлекай только то, что явно есть в тексте — не придумывай факты.\n"
    "2. НЕ извлекай числа, единицы измерения и числовые ограничения (это делает "
    "отдельный модуль правилами) — Property называй по имени параметра без значения.\n"
    "3. Имя сущности — как в тексте (предпочтительно RU); в \"synonyms\" добавляй "
    "англ./сокращённый вариант, если он встречается рядом.\n"
    "4. source/target связи — это ИМЕНА сущностей из твоего же списка \"entities\" "
    "(точное совпадение строки name), связи с сущностями вне списка не создавай.\n"
    "5. confidence — число 0..1, насколько явно текст утверждает связь (0.9+ — "
    "прямое утверждение, 0.5 — косвенное/предположительное).\n"
    "6. Если в тексте нет сущностей онтологии — верни пустые списки, не выдумывай.\n\n"
    "Ответ — ТОЛЬКО валидный JSON без markdown-обёртки, комментариев и пояснений, "
    "строго такой формы:\n"
    "{\n"
    '  "entities": [{"name": "...", "type": "...", "synonyms": ["..."], "attrs": {}}],\n'
    '  "relations": [{"source": "...", "target": "...", "type": "...", "confidence": 0.8}]\n'
    "}"
)


# ─── build_user_prompt ──────────────────────────────────────────────────────
# Назначение: оборачивает текст чанка в пользовательский промпт для /api/chat.
# Входные связи: текст чанка (contracts.Chunk.text)
# Выходные данные: str — user-сообщение
# Уровень: ✅ реализовано (A-08)
def build_user_prompt(chunk_text: str) -> str:
    return f"Фрагмент текста:\n\n{chunk_text}\n\nИзвлеки сущности и связи в формате JSON, как указано выше."
